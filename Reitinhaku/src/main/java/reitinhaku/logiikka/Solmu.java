package reitinhaku.logiikka;

/**
 *
 * @author Samuel
 */
public class Solmu {

    private int paino;
    private int x;
    private int y;

    /**
     *
     * @param x
     * @param y
     * @param paino
     */
    public Solmu(int x, int y, int paino) {
        this.x = x;
        this.y = y;
        this.paino = paino;
    }

    /**
     *
     * @return
     */
    /**
     *
     * @return
     */
    public int getX() {
        return x;
    }

    /**
     *
     * @return
     */
    public int getY() {
        return y;
    }

    /**
     *
     * @return
     */
    public int getPaino() {
        return paino;
    }


}

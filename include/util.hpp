/**
 * \file util.hpp
 * Utility macros.
 */


#ifndef UTIL_HPP
#define UTIL_HPP

/**
 * Macro for minimum of a and b.
 */
#define MIN(a, b) ((a) < (b) ? (a) : (b))
/**
 * Macro for maximum of a and b.
 */
#define MAX(a, b) ((a) > (b) ? (a) : (b))
/**
 * Macro for absolute value of a.
 */
#define ABS(a) ((a) >= 0 ? (a) : (-(a)))

#endif
